package core;

/**
 * ChoixFinPartie est une énumération permettant de stocker le choix de l'utilisateur suite à la fin d'une Partie.<br>
 * L'utilisateur pourra donc rejouer selon les mêmes paramètres, faire une nouvelle partie ou quitter.<br>
 * 
 */
public enum ChoixFinPartie {
    REJOUER,
    NOUVELLE_PARTIE,
    QUITTER;
    
}
